public class Filme {
  private String titulo;
  private int ano;
  private int tipo;

  public void setTitulo(String titulo){
    this.titulo = titulo;
  }

  public String getTitulo(){
    return titulo;
  }

  public void setAno(int ano){
    this.ano = ano;
  }

  public int getAno(){
    return ano;
  }

  public void setTipo(int tipo){
    this.tipo = tipo;
  }

  public int getTipo(){
    return tipo;
  }
}